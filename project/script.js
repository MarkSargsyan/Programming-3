var side = 10;
var grassArr = [];
var xotakerArr = [];
var gishatichArr = [];
var mardArr = [];
var amenakerArr = [];
var xotiQanak = 0;

function setup() {
    frameRate(60);
    createCanvas(matrix[0].length * side, matrix.length * side);
    background("#767676");
    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {
            if (matrix[y][x] == 1) {
                var xot = new Grass(x, y);
                grassArr.push(xot);
            }
            else if (matrix[y][x] == 2) {
                var xotaker = new Xotaker(x, y);
                xotakerArr.push(xotaker);
            }
            else if (matrix[y][x] == 3) {
                var gishatich = new Gishatich(x, y);
                gishatichArr.push(gishatich);
            }
            else if (matrix[y][x] == 4) {
                var mard = new Mard(x, y);
                mardArr.push(mard);
            }
            else if (matrix[y][x] == 5) {
                var amenaker = new Amenaker(x, y);
                amenakerArr.push(amenaker);
            }
        }
    }
}

function draw() {
    xotiQanak = 0;
    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {
            if (matrix[y][x] == 1) {
                xotiQanak++;
                fill("green");
                stroke(20);
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 0) {
                fill("#767676");
                noStroke();
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 2) {
                fill("yellow");
                stroke(20);
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 3) {
                fill("red");
                stroke(20);
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 4) {
                fill("white");
                stroke(20);
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 5) {
                fill("black");
                stroke(20);
                rect(x * side, y * side, side, side);
            }
        }
    }

    for (var x in grassArr) {
        grassArr[x].bazmanal();
    }

    for (var y in xotakerArr) {
        xotakerArr[y].utel();
    }
    for (var j in gishatichArr) {
        gishatichArr[j].utel();
    }
    for (var l in mardArr) {
        mardArr[l].utel();
    }
    for (var z in amenakerArr) {
        amenakerArr[z].utel();
    }
}